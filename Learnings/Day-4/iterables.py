class Count:
    def __init__(self, start, end):
        self.current = start
        self.end = end
        
    def __init__(self):
        return self
    
    def __init__(next):
        if self.start > self.end:
            raise stopIteration
        value = self.current
        self.current += 1
        return value
    
c = Count(1, 3)

